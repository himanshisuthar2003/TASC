    <p>
        <strong> Honesty and integrity<br></strong>
        It is of paramount importance that the public has faith in the honesty and integrity of police officers. Officers should therefore be open and truthful in their dealings; avoid being improperly beholden to any person or institution; and discharge their duties with integrity.<br><br>

        <strong> Fairness and impartiality<br></strong>
        Police officers have a particular responsibility to act with fairness and impartiality in all their dealings with the public and their colleagues.<br><br>

        <strong> Politeness and tolerance<br></strong>
        Officers should treat members of the public and colleagues with courtesy and respect, avoiding abusive or deriding attitudes or behaviour. In particular, officers must avoid: favouritism of an individual or group; all forms of harassment, victimisation or unreasonable discrimination; and overbearing conduct to a colleague, particularly to one junior in rank or service.<br><br>

        <strong>Use of force and abuse of authority<br></strong>
        Officers must never knowingly use more force than is reasonable, nor should they abuse their authority<br><br>

        <strong>Performance of duties<br></strong>
        Officers should be conscientious and diligent in the performance of their duties. Officers should attend work promptly when rostered for duty. If absent through sickness or injury, they should avoid activities likely to retard their return to duty<br>
        <br>
        <strong> Lawful orders<br></strong>
        The police service is a disciplined body. Unless there is good and sufficient cause to do otherwise, officers must obey all lawful orders and abide by the provisions of legislation applicable to the police. Officers should support their colleagues in the execution of their lawful duties, and oppose any improper behaviour, reporting it where appropriate.<br>

        <strong> Confidentiality<br></strong>
        Information which comes into the possession of the police should be treated as confidential. It should not be used for personal benefit and nor should it be divulged to other parties except in the proper course of police duty. Similarly, officers should respect, as confidential, information about force policy and operations unless authorised to disclose it in the course of their duties.
        <br><br>
        <strong> Criminal offences<br></strong>
        Officers must report any proceedings for a criminal offence taken against them. Conviction of a criminal offence or the administration of a caution may of itself result in further action being taken.
        <br><br>
        <strong> Property<br></strong>
        Officers must exercise reasonable care to prevent loss or damage to property (excluding their own personal property but including police property).
        <br><br>
        <strong> Sobriety<br></strong>
        Whilst on duty officers must be sober. Officers should not consume alcohol when on duty unless specifically authorised to do so or it becomes necessary for the proper discharge of police duty.
